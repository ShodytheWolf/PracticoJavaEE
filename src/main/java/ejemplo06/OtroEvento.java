package ejemplo06;

public class OtroEvento {
	private String descripcion;
	private int valor;

	public OtroEvento(String desc, int value) {
		this.descripcion = desc;
		this.valor = value;
	}
	
	public String getDescripcion() {
		return descripcion;
	}

	public int getValor() {
		return valor;
	}
}
